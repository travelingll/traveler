package kr.comm.vo;

public class CommReplyVO {
	private int comm_renum;
	private String comm_recontent;
	private String comm_redate;
	private String comm_remodifydate;
	private String comm_reip;
	private int comm_num;
	private int mem_num;
	private String id;
	
	
	public int getComm_renum() {
		return comm_renum;
	}
	public void setComm_renum(int comm_renum) {
		this.comm_renum = comm_renum;
	}
	public String getComm_recontent() {
		return comm_recontent;
	}
	public void setComm_recontent(String comm_recontent) {
		this.comm_recontent = comm_recontent;
	}
	public String getComm_redate() {
		return comm_redate;
	}
	public void setComm_redate(String comm_redate) {
		this.comm_redate = comm_redate;
	}
	public String getComm_remodifydate() {
		return comm_remodifydate;
	}
	public void setComm_remodifydate(String comm_remodifydate) {
		this.comm_remodifydate = comm_remodifydate;
	}
	public String getComm_reip() {
		return comm_reip;
	}
	public void setComm_reip(String comm_reip) {
		this.comm_reip = comm_reip;
	}
	public int getComm_num() {
		return comm_num;
	}
	public void setComm_num(int comm_num) {
		this.comm_num = comm_num;
	}
	public int getMem_num() {
		return mem_num;
	}
	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
}
